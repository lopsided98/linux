void aai_reset_regs(struct card_data_t *aai)
{
	int i;
	struct aai_reset_reg_t *reg;

	for (i = 0;
	     i < sizeof(reset_regs) / sizeof(struct aai_reset_reg_t);
	     i++) {
		reg = &reset_regs[i];
		aai_writereg(aai, reg->reset, reg->addr, 0);
	}
}

